package DSA.asm;

public class SumOfIntegers {


    public static void main(String[] args) {
        int a = 5; // Variable assignment (constant time)
        int b = 10; // Variable assignment (constant time)

        int sum = a + b; // Addition operation (constant time)
        String result = "Sum of a and b: " + sum; // Concatenation (constant time)
        System.out.println(result); // Print statement (constant time)
    }
}